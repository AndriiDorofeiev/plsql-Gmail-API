CREATE TABLE "MESSAGES" (
	"ID" VARCHAR2(20),
	"THREADID" VARCHAR2(20),
	"LABELIDS" VARCHAR2(512),
	"SNIPPET" VARCHAR2(1024),
	"PARTID" VARCHAR2(1024),
	"FILENAME" VARCHAR2(1024),
	"DELIVERED_TO" VARCHAR2(1024),
	"MESSAGE_DATE" VARCHAR2(50),
	"MESSAGE_FROM" VARCHAR2(1024),
	"SUBJECT" VARCHAR2(4000),
	"MESSAGE_TO" VARCHAR2(4000 CHAR),
	"BODY_SIZE" NUMBER,
	"SIZEESTIMATE" NUMBER,
	"HISTORYID" VARCHAR2(30),
	"EMAIL_BODY" CLOB,
	"INTERNALDATE" TIMESTAMP (6),
	"MODIFY_TIME" DATE DEFAULT SYSDATE NOT NULL ENABLE,
	"MIME_TYPE" VARCHAR2(128 CHAR),
	"MARKED_FOR_DELETE" CHAR(1) DEFAULT 'N' NOT NULL ENABLE,
	"DELETED_AT" DATE,
	CONSTRAINT "MESSAGES_PK" PRIMARY KEY ("ID") USING INDEX ENABLE
);